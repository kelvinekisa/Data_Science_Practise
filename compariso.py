# RELATIONAL OPERATORS
# used for comparing values

a = 9
b = 5
# 1. Greater than
print(a > b)       # True

# 2. Less than
print(a < b)        # False

# 3. Equal to
print(a == b)       # False

# 4. Not equal to
print(a != b)       # True

# 5. Greater than or equal to
print(a >= b)       # True

# 6. Less than or equal to
print(a <= b)       # False


